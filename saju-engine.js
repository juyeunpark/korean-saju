export function analyzeSaju(dateStr, timeStr) {
  const date = new Date(dateStr);
  const year = date.getFullYear();
  const month = date.getMonth() + 1;
  const day = date.getDate();
  const hour = timeStr ? parseInt(timeStr.split(':')[0]) : 12;

  const weights = {
    [ELEMENTS.WOOD]: (year % 5 === 0 ? 25 : 5) + (month >= 2 && month <= 4 ? 20 : 5) + (day % 5 === 0 ? 15 : 0),
    [ELEMENTS.FIRE]: (year % 5 === 1 ? 25 : 5) + (month >= 5 && month <= 7 ? 20 : 5) + (day % 5 === 1 ? 15 : 0),
    [ELEMENTS.EARTH]: (year % 5 === 2 ? 25 : 5) + ([1, 4, 7, 10].includes(month) ? 20 : 5) + (day % 5 === 2 ? 15 : 0),
    [ELEMENTS.METAL]: (year % 5 === 3 ? 25 : 5) + (month >= 8 && month <= 10 ? 20 : 5) + (day % 5 === 3 ? 15 : 0),
    [ELEMENTS.WATER]: (year % 5 === 4 ? 25 : 5) + (month >= 11 || month <= 1 ? 20 : 5) + (day % 5 === 4 ? 15 : 0)
  };

  const dayElementIndex = (day + month + year) % 5;
  const dayMaster = Object.values(ELEMENTS)[dayElementIndex];
  weights[dayMaster] += 45;

  const total = Object.values(weights).reduce((a, b) => a + b, 0);
  const normalized = {};
  for (let key in weights) {
    normalized[key] = Math.round((weights[key] / total) * 100);
  }

  const sorted = Object.entries(normalized).sort((a, b) => b[1] - a[1]);
  return {
    dayMaster,
    balance: normalized,
    dominant: sorted[0][0],
    secondDominant: sorted[1][0],
    lacking: sorted[4][0],
    raw: sorted
  };
}

