export const ELEMENTS = {
  WOOD: '목(木)',
  FIRE: '화(火)',
  EARTH: '토(土)',
  METAL: '금(金)',
  WATER: '수(水)'
};

export function analyzeSaju(dateStr, timeStr) {
  const date = new Date(dateStr);
  const year = date.getFullYear();
  const month = date.getMonth() + 1;
  const day = date.getDate();

  const weights = {
    [ELEMENTS.WOOD]: (year % 5 === 0 ? 25 : 5) + (month >= 2 && month <= 4 ? 20 : 5),
    [ELEMENTS.FIRE]: (year % 5 === 1 ? 25 : 5) + (month >= 5 && month <= 7 ? 20 : 5),
    [ELEMENTS.EARTH]: (year % 5 === 2 ? 25 : 5) + ([1, 4, 7, 10].includes(month) ? 20 : 5),
    [ELEMENTS.METAL]: (year % 5 === 3 ? 25 : 5) + (month >= 8 && month <= 10 ? 20 : 5),
    [ELEMENTS.WATER]: (year % 5 === 4 ? 25 : 5) + (month >= 11 || month <= 1 ? 20 : 5)
  };

  const dayElementIndex = (day + month + year) % 5;
  const dayMaster = Object.values(ELEMENTS)[dayElementIndex];
  weights[dayMaster] += 40;

  const sorted = Object.entries(weights).sort((a, b) => b[1] - a[1]);
  return {
    dayMaster,
    dominant: sorted[0][0],
    lacking: sorted[4][0]
  };
}

